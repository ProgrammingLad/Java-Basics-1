package UpcastingAndDowncasting;

public class AnimalDog extends AnimalParent {
	public void Animal(){
		System.out.println("I am a dog");
	}
	
	public void AnimalName() {
		System.out.println("My name is: Doggy");
	}
	
	public void AnimalAction() {
		System.out.println("I do: running or walking");
	}
	
	public void AnimalLive(){
		System.out.println("I live in: land");
	}
	
	public void Bark() {
		System.out.println("Woof Woof");
	}
}
