package UpcastingAndDowncasting;

public class AnimalBird extends AnimalParent {
	public void Animal(){
		System.out.println("I am a bird");
	}
	
	public void AnimalName() {
		System.out.println("My name is: Birdy");
	}
	
	public void AnimalAction() {
		System.out.println("I do: Flying");
	}
	
	public void AnimalLive(){
		System.out.println("I live in the skies");
	}
	
	public void Chirp() {
		System.out.println("Chirp chirp");
	}
}
